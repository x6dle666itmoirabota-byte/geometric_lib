def area(a, b):
    """
    Вычисляет площадь прямоугольника по двум сторонам a и b.

    Пример вызова:
    >>> area(5, 10)
    50
    """
    return a * b

def perimeter(a, b):
    """
    Вычисляет периметр прямоугольника по двум сторонам a и b.

    Пример вызова:
    >>> perimeter(5, 10)
    30
    """
    return 2 * (a + b)